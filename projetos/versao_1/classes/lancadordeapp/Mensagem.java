package lancadordeapp;

public class Mensagem {

	
	private String status;	
	private String nome;
	private String compilacao;
	private String nome_sistema;
	private String data_cadastro;
	private String versao;
	private String tamanho;
	
	
	
	
	public String getStatus() {	return status;}
	public void setStatus(String status) { this.status = status;}
	
	public String getNome() {	return nome;}
	public void setNome(String nome) {		this.nome = nome;}
	
	public String getCompilacao() {	return compilacao;}
	public void setCompilacao(String compilacao) {	this.compilacao = compilacao;}
	
	public String getNome_sistema() {	return nome_sistema;}
	public void setNome_sistema(String nome_sistema) {	this.nome_sistema = nome_sistema;}
	
	public String getData_cadastro() {	return data_cadastro;}
	public void setData_cadastro(String data_cadastro) {	this.data_cadastro = data_cadastro;}
	
	public String getVersao() {	return versao;}
	public void setVersao(String versao) {		this.versao = versao;}
	
	public String getTamanho() {	return tamanho;}
	public void setTamanho(String tamanho) {	this.tamanho = tamanho;}
	
		
	
	
}
